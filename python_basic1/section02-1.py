#Separator 옵션 사용

# 연결하는 옵션- 자바스크립트의 join() 메소드와과 유사.
print("T", "E", "S", "T", sep="");

#End 옵션 사용
# 출력을 연결하느 옵션;
print("welcome to", end="");
print("the black paradice", end="");

# format 사용 [], {}, ();
print("{} and []" .format("you", "me"));
print("{0} and {1} and {0}" .format("1","2"));
print("{a} and {b}" .format(a="1", b="aw"));

# %s: 문자, %d: 정수, %f: 실수
print("%s favorite number is %d" % ("awdaw", 123));


